				<h2>The Challenge</h2>
				
				<p>The New York Public Library (NYPL) was providing computer access and training for tens of thousands of senior citizens each year, but lacked age-specific curriculum and methodology to optimize the experience of older library visitors.</p>
				 
				<h2>The Strategy</h2>
				
				<p>Utilize the time-tested OATS instructional methodology and curriculum, while adapting specific content to the needs of the NYPL.  Build the capacity of library staff through direct training and observation.</p>
				 
				<h2>The Solution</h2>
				
				<p>OATS developed and delivered a customized six-session course, NYPL Website Basics for Older Adults, for older adult library members at NYPL branches.  The new class included help on accessing the library website, searching the catalog, setting up and using an NYPL online account, and finding classes and programs at the libraries. Course materials and training were made available in English and Spanish.</p>
				 
				<p>In addition, OATS delivered its ten-week Computer Basics for Older Adults at multiple library locations and invited local library staff to observe and participate in class instruction.  OATS curriculum was licensed to the NYPL for use by trained staff, and made available system-wide.  NYPL received highly positive feedback from older adults who participated in the program and extended their grant funding to continue the initiative past the original contracted period.</p>
	
