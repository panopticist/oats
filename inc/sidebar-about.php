
				<h3>About Us</h3>
				<p>Older Adults Technology Services (OATS) is a 501(c)(3) nonprofit organization that harnesses the power of technology to change the way we age. Since 2004, OATS has:</p>

				<ul>
					<li>Trained over 7,000 older adults</li>
					<li>Provided over 10,000 classes</li>
					<li>Partnered with over 80 nonprofit organizations to deliver training</li>
					<li>Developed more than 3,000 pages of curriculum around the technology needs of seniors</li>
				</ul>
